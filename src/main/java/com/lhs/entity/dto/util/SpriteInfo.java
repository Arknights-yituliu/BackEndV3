package com.lhs.entity.dto.util;

public class SpriteInfo {
    private String inputDir;
    private String outputDir;
    private String spriteFilename;
    private String cssFilename;
    private String cosFileType;


    public String getInputDir() {
        return inputDir;
    }

    public void setInputDir(String inputDir) {
        this.inputDir = inputDir;
    }

    public String getOutputDir() {
        return outputDir;
    }

    public void setOutputDir(String outputDir) {
        this.outputDir = outputDir;
    }

    public String getSpriteFilename() {
        return spriteFilename;
    }

    public void setSpriteFilename(String spriteFilename) {
        this.spriteFilename = spriteFilename;
    }

    public String getCssFilename() {
        return cssFilename;
    }

    public void setCssFilename(String cssFilename) {
        this.cssFilename = cssFilename;
    }

    public String getCosFileType() {
        return cosFileType;
    }

    public void setCosFileType(String cosFileType) {
        this.cosFileType = cosFileType;
    }


}
