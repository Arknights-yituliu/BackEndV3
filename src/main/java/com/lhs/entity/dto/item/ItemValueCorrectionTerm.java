package com.lhs.entity.dto.item;

public class ItemValueCorrectionTerm {
    private Double correctionTerm;
    private String stageCode;
    private String seriesId;
    private String seriesName;

    public Double getCorrectionTerm() {
        return correctionTerm;
    }

    public void setCorrectionTerm(Double correctionTerm) {
        this.correctionTerm = correctionTerm;
    }

    public String getStageCode() {
        return stageCode;
    }

    public void setStageCode(String stageCode) {
        this.stageCode = stageCode;
    }

    public String getSeriesId() {
        return seriesId;
    }

    public void setSeriesId(String seriesId) {
        this.seriesId = seriesId;
    }

    public String getSeriesName() {
        return seriesName;
    }

    public void setSeriesName(String seriesName) {
        this.seriesName = seriesName;
    }




}
